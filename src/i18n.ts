export function getLang(): Lang {
  const cfg = vscode.workspace.getConfiguration('gitscribe');
  return (cfg.get<string>('language') as Lang) || 'en';
}

export function t(key: string): string {
  const lang = getLang();
  return T[lang][key] || key;
}
